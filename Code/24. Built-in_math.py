# Python has Built in Math functions that allows you to performs various mathematical tasks.
# also an built-in module to import functions.

# Built-in functions.
# Example : min() and max()
x = min(5, 3,20)
y = max(8, 10, 8)

print(x)
print(y)

# using abs() method returns the absolute (positive) value of the specified number.
x = abs(-7.25)
print(x)
# output(7.25)

# using pow() method, return value of x to the power of y in case of pow(x, y)
x = pow(4, 3)
print(x)

# using modules
import math
x = math.sqrt(64)   # <-- provide value as arguement
print(x)
y = 9.4
z = math.ceil(y)
print(z)
z = math.floor(y)
print(z)

# check pi value
x = math.pi()
print(x)
